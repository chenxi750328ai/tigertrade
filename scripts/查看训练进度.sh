#!/bin/bash
# 查看SIL2603训练进度

echo "================================================================================"
echo "📊 SIL2603训练进度监控"
echo "================================================================================"
echo ""

# 检查进程是否运行
if [ -f sil2603_training.pid ]; then
    PID=$(cat sil2603_training.pid)
    if ps -p $PID > /dev/null 2>&1; then
        echo "✅ 训练正在运行中（进程ID: $PID）"
    else
        echo "⚠️ 训练进程已结束"
    fi
else
    echo "⚠️ 未找到进程ID文件"
fi

echo ""
echo "================================================================================"
echo "最新日志（实时更新，按Ctrl+C退出）:"
echo "================================================================================"
echo ""

# 实时显示日志
tail -f sil2603_training.log
